void MSC_Application(char* Text, int size, int mode)
{

  uint32_t byteswritten;              // File write counts

  // Mount the file system
  if (f_mount(&USBDISKFatFs, (TCHAR const*)USBDISKPath, 0) != FR_OK)
  {
    // FatFs Initialization Error
    USB_Error_Handler();
  }
  else
  {
	if (mode == 1){
		f_open(&MyFile, "Data.CSV", FA_WRITE | FA_CREATE_ALWAYS);
	}
	else{
		f_open(&MyFile, "Data.CSV", FA_WRITE | FA_OPEN_APPEND);
	}
    // Write data to the file
    f_write(&MyFile, Text, size, (void *)&byteswritten); // Use sizeof(wtext)-1 to exclude null terminator

    // Close the file after writing
    f_close(&MyFile);

  }

  // Unmount the file system
  FATFS_UnLinkDriver(USBDISKPath);
}

